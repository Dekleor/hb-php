<?php

class Beanie
{
    public $cat;
    public $pic;
    public $name;
    public $price;
    public $desc;

    public function getCat()
    {
        return $this->cat;
    }
    public function getPic()
    {
        return $this->pic;
    }
    public function getName()
    {
        return $this->name;
    }
    public function getPrice()
    {
        return $this->price;
    }
    public function getDesc()
    {
        return $this->desc;
    }
}